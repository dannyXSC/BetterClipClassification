# 获得每个 item（比如 circle）属于哪个大类（比如 shape）
item_to_class = {}
for cls_nm, dic in raw_class.items():
    # 小写
    cls_nm = cls_nm.lower()
    for item_nm, item in dic.items():
        for nm in item:
            # 小写
            nm = nm.lower()
            item_set.add(nm)
            if nm in item_to_class:
                item_to_class[nm].add(cls_nm)
            else:
                item_to_class[nm] = set()
                item_to_class[nm].add(cls_nm)
